
public class operacionesMate {

	public static void main(String[] args) {
		
		//declarar variables 
		double n1=0;
		double n2=0;
		double res=0;
		
		//Suma 
		n1=40;
		n2=77.33;
		res=n1+n2;
		//Imprimir resultado 

		System.out.println("\n El resultado de " + n1 + " + " + n2 + " = " + res);
		
		//Resta 
		n1=40;
		n2=70;
		res=n1-n2;
		//Imprimir resultado 
		System.out.println("\n El resultado de " + n1 + " - " + n2 + " = " + res);
		
		//Multiplicación 
		n1=2;
		n2=23;
		res=n1*n2;
		//Imprimir resultado 
		System.out.println("\n El resultado de " + n1 + " * " + n2 + " = " + res);
		
		//División 
		n1=40;
		n2=10.5;
		res=n1/n2;
		//Imprimir resultado 
		System.out.println("\n El resultado de " + n1 + " / " + n2 + " = " + res);
		
	}

}
